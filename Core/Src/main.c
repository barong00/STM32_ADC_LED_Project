/**
  * @brief ADC'den dijital değer okuma fonksiyonu
  * @retval uint32_t: Okunan dijital ADC değeri
  */
uint32_t Read_ADC_Value(void) {
    HAL_ADC_Start(&hadc1);  // ADC'yi başlat
    if (HAL_ADC_PollForConversion(&hadc1, 10000) == HAL_OK) {  // Dönüşüm tamamlanana kadar bekle
        return HAL_ADC_GetValue(&hadc1);  // Dijital ADC değerini al
    }
    return 0;  // Başarısız olursa 0 dön
}

/**
  * @brief Dijital ADC değerini voltaja çevirme fonksiyonu
  * @param uint32_t adc_value: Okunan dijital ADC değeri
  * @retval float: Hesaplanan voltaj değeri
  */
float Convert_ADC_to_Voltage(uint32_t adc_value) {
    return ((float)adc_value) * 3.3f / 4095.0f;  // 12-bit ADC değerini 0-3.3V voltaja dönüştür
}

